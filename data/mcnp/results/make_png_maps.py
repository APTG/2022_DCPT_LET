#!/usr/bin/env python3
"""
make_png_maps.py

Renders xy_map.dat (lateral) and xz_map.dat (longitudinal), as produced by
parse_mdata.py, into PNG images. Each input file is a flat
"x_center y_center value" (or "x_center z_center value") table on a regular
grid; the grid is reconstructed from the unique x/y (or x/z) values rather
than assuming a fixed shape, so it doesn't need to know mesh dimensions.
"""
import sys
from pathlib import Path

try:
    import numpy as np
    import matplotlib
    matplotlib.use('Agg')
    import matplotlib.pyplot as plt
except ImportError as e:
    print(f"error: missing dependency ({e}) - install numpy and matplotlib "
          f"to enable PNG map generation", file=sys.stderr)
    sys.exit(1)


def plot_map(datfile, pngfile, xlabel, ylabel):
    """
    Styled to match the OpenShieldHIT/SHIELD-HIT12A preview images, which are
    generated by pymchelper's `convertmc image` converter
    (pymchelper/writers/plots.py). Matched directly against that source, not
    by eye:
      - cmap "gnuplot2" - pymchelper's documented 2D-heatmap default
        (https://datamedsci.github.io/pymchelper/image_converter.html)
      - pcolorfast, NOT pcolormesh, and no set_aspect('equal') call anywhere
        in their code - the image fills the axes box as-is rather than
        preserving the physical X:Z data ratio. Matching that here (no
        aspect override) rather than our previous aspect='equal', which was
        the actual cause of the plot looking a different shape/size.
      - no custom figsize anywhere in their file -> plain matplotlib default
        (6.4, 4.8) in, used here instead of our previous custom (6, 5).
      - colorbar label explicitly rotation=270, va='bottom' (their exact
        cbar.set_label call), not matplotlib's default label orientation.
    "DOSE [MeV/g/prim]" colorbar text matches this manifest's
    per_source_particle normalization; no title, same as theirs.
    """
    data = np.loadtxt(datfile)
    if data.size == 0:
        print(f"warning: {datfile} is empty, skipping", file=sys.stderr)
        return
    x, y, v = data[:, 0], data[:, 1], data[:, 2]
    xu = np.unique(x)
    yu = np.unique(y)
    grid = np.full((len(yu), len(xu)), np.nan)
    xi = {val: i for i, val in enumerate(xu)}
    yi = {val: i for i, val in enumerate(yu)}
    for xv, yv, vv in zip(x, y, v):
        grid[yi[yv], xi[xv]] = vv

    fig, ax = plt.subplots()  # default figsize, matching pymchelper
    xspan = [xu.min(), xu.max()]
    yspan = [yu.min(), yu.max()]
    im = ax.pcolorfast(xspan, yspan, grid, cmap='gnuplot2',
                        vmin=0, vmax=np.nanmax(grid))
    ax.set_xlabel(xlabel)
    ax.set_ylabel(ylabel)
    ax.grid(True, alpha=0.3)
    cbar = fig.colorbar(im, ax=ax)
    cbar.set_label('DOSE [MeV/g/prim]', rotation=270, verticalalignment='bottom')
    fig.tight_layout()
    fig.savefig(pngfile)  # no dpi override - matches pymchelper's default (100 dpi, 640x480px)
    plt.close(fig)
    print(f"wrote {pngfile}")


def main():
    outdir = Path(sys.argv[1])
    xy = outdir / "xy_map.dat"
    xz = outdir / "xz_map.dat"
    if xy.exists():
        # filename must contain "XY" (case-sensitive) - build_pages_site.py's
        # preview-image grouping matches on that literal substring in the
        # filename to sort lateral vs longitudinal maps into the right
        # section on the plan page.
        plot_map(xy, outdir / "xy_map_XY.png", "Position (X) [cm]", "Position (Y) [cm]")
    else:
        print(f"note: {xy} not found, skipping xy_map_XY.png", file=sys.stderr)
    if xz.exists():
        # filename must contain "XZ" for the same reason.
        plot_map(xz, outdir / "xz_map_XZ.png", "Position (X) [cm]", "Position (Z) [cm]")
    else:
        print(f"note: {xz} not found, skipping xz_map_XZ.png", file=sys.stderr)


if __name__ == '__main__':
    main()
